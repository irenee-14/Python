# 176 // 16
# 177 // 1
# 16 + 1 = 17

A, B = map(int, input().split())
# 176 177
# 175! 177!
A -= 1

# 1로 나누었을 때의 값을 더해주기
tmp_A = A
for i in range(1, 99):
    tmp_A += (A // (2 ** i)) * ((2 ** i) - (2 ** (i - 1)))
    # 2, 4, 8, ... 을 돌면서
    # A // 2 * 2 + 1
    # A // 4 * 4 + 2

tmp_B = B
for i in range(1, 99):
    tmp_B += (B // (2 ** i)) * ((2 ** i) - (2 ** (i - 1)))

print(tmp_B - tmp_A)